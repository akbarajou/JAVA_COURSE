public class Operators {

    // Addition operator
    public String addition (int firstArgs, int secondArgs ) {

        return firstArgs + " + " + secondArgs + " = " + (firstArgs + secondArgs);
    }

    // Subtraction operator
    public String subtraction (int firstArgs, int secondArgs ) {

        return firstArgs + " - " + secondArgs + " = " + (firstArgs - secondArgs);
    }

    // Multiplication operator
    public String multiplication (int firstArgs, int secondArgs ) {

        return firstArgs + " * " + secondArgs + " = " + (firstArgs * secondArgs);
    }

    // Division operator
    public String division (int firstArgs, int secondArgs ) {

        return firstArgs + " / " + secondArgs + " = " + (firstArgs / secondArgs);
    }



}
